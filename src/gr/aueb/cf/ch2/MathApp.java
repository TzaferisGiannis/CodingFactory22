package gr.aueb.cf.ch2;

/**
 * Demostrates the Math class.
 */
public class MathApp {

    public static void main(String[] args) {
        System.out.println("To απόλυτο το -5 είναι: " + Math.abs (-5));
        System.out.println("Το min μεταξύ του 1,10 είναι: " + Math.min(1, 10 ));
        System.out.println("Το max μεταξύ του 1,10 είναι: " + Math.max(1, 10 ));
    }
}
